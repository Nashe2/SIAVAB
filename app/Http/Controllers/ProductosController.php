    {
        //
        $options=[
            'title'=>$request->title,
            'description'=>$request->description,
            'price'=>$request->price,
            'cantidad'=>$request->cantidad,
        ];

        if(Producto::create($options)){
            return redirect('/');
        }else{
            return view('productos.create');
        }
    }

